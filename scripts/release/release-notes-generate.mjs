#!/usr/bin/env node
import { readFile, writeFile, mkdir } from 'node:fs/promises'
import { resolve } from 'node:path'
const root = process.cwd()
let acceptance = { status: 'unknown', passedStepCount: 0, stepCount: 0 }
try { acceptance = JSON.parse(await readFile(resolve(root, 'docs/kernel/generated-acceptance-report.json'), 'utf8')) } catch {}
const notes = ['# Generated Release Notes', '', '- Package: pandoshare-agent', '- Acceptance: ' + acceptance.passedStepCount + '/' + acceptance.stepCount + ' ' + acceptance.status, '- Security: baseline report required before release', '- Known limitations: see docs/release/known-limitations.md', '', 'This file is generated by scripts/release/release-notes-generate.mjs.', ''].join('\n')
await mkdir(resolve(root, 'docs/release'), { recursive: true })
await writeFile(resolve(root, 'docs/release/generated-release-notes.md'), notes, 'utf8')
console.log('release notes generated')
